    private void btnEmptyBinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmptyBinActionPerformed
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to PERMANENTLY delete all items in trash?", "Warning", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        try (java.sql.Connection conn = java.sql.DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD)) {
            String sql = "DELETE FROM TRASH";
            conn.prepareStatement(sql).executeUpdate();
            fetchTrash();
            javax.swing.JOptionPane.showMessageDialog(this, "Trash bin cleared!");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }// TODO add your handling code here:
    }//GEN-LAST:event_btnEmptyBinActionPerformed

    private void btnRestoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestoreActionPerformed
    int row = jTable1.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a book to restore!");
        return;
    }
    
    String id = jTable1.getValueAt(row, 0).toString();
    
    try (java.sql.Connection conn = java.sql.DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD)) {
        // 1. BOOKS tablosuna geri gönder
        String restoreSql = "INSERT INTO BOOKS SELECT BOOK_ID, CATEGORY, NAME, AUTHOR, COPIES, PAGES, PUBLISH_YEAR FROM TRASH WHERE BOOK_ID = ?";
        java.sql.PreparedStatement pst1 = conn.prepareStatement(restoreSql);
        pst1.setString(1, id);
        pst1.executeUpdate();
        
        // 2. TRASH tablosundan sil
        String deleteSql = "DELETE FROM TRASH WHERE BOOK_ID = ?";
        java.sql.PreparedStatement pst2 = conn.prepareStatement(deleteSql);
        pst2.setString(1, id);
        pst2.executeUpdate();
        
        fetchTrash(); // Tabloyu tazele
        javax.swing.JOptionPane.showMessageDialog(this, "Book successfully restored to Library!");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Restore Error: " + e.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnRestoreActionPerformed

