    private void fetchTrash() {
    try (java.sql.Connection conn = java.sql.DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD)) {
        String sql = "SELECT * FROM TRASH";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();
        
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("BOOK_ID"),
                rs.getString("CATEGORY"),
                rs.getString("NAME"),
                rs.getString("AUTHOR"),
                rs.getInt("COPIES"),
                rs.getInt("PAGES"),
                rs.getInt("PUBLISH_YEAR")
            });
        }
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Trash Loading Error: " + e.getMessage());
    }
}
