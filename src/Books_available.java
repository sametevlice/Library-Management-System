    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = txtbookid.getText();
        String sql = "UPDATE BOOKS SET CATEGORY=?, NAME=?, AUTHOR=?, COPIES=?, PAGES=?, PUBLISH_YEAR=? WHERE BOOK_ID=?";

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to update this book?", "Update Confirm", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
                PreparedStatement pst = conn.prepareStatement(sql)) {

                pst.setString(1, comboCategory.getSelectedItem().toString());
                pst.setString(2, txtbookname.getText());
                pst.setString(3, txtauthor.getText());
                pst.setInt(4, Integer.parseInt(txtcopies.getText()));
                pst.setInt(5, Integer.parseInt(txtpages.getText()));
                pst.setInt(6, Integer.parseInt(txtyear.getText()));
                pst.setString(7, id);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Updated successfully!");
                fetchBooks(); // Tabloyu yenile
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Update Error: " + e.getMessage());
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

