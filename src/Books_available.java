    private void backupBeforeDeleteAll() {
    try (java.io.FileWriter fw = new java.io.FileWriter("Trash_Bin.txt", true)) {
        fw.write("\n--- BULK DELETE OPERATION (" + new java.util.Date() + ") ---\n");
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            // Using indices based on our Madde 2 Checkbox structure (0: Select, 1: ID, 3: Name)
            fw.write("ID: " + jTable1.getValueAt(i, 1) + " | Title: " + jTable1.getValueAt(i, 3) + "\n");
        }
        fw.write("------------------------------------------\n");
    } catch (Exception e) {
        System.out.println("Backup error: " + e.getMessage());
    }
}
