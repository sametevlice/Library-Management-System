    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        // Madde 6: Verilerin dosyaya yazılması (Zapis danych do pliku)
        try (java.io.FileWriter fw = new java.io.FileWriter("Search_Results_Backup.txt")) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            fw.write("--- SAMET'S LIBRARY SEARCH RESULTS ---\n");
            fw.write("ID | Category | Name | Author | Pages\n");
            fw.write("--------------------------------------\n");

            for (int i = 0; i < model.getRowCount(); i++) {
                String line = model.getValueAt(i, 0) + " | " +
                model.getValueAt(i, 1) + " | " +
                model.getValueAt(i, 2) + " | " +
                model.getValueAt(i, 3) + " | " +
                model.getValueAt(i, 5) + "\n";
                fw.write(line);
            }

            JOptionPane.showMessageDialog(this, "Data successfully saved to Search_Results_Backup.txt!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "File Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExportActionPerformed

