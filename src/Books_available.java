private void fetchBooks() {
    try (Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
         PreparedStatement pst = conn.prepareStatement("SELECT BOOK_ID, CATEGORY, NAME, AUTHOR, COPIES, PAGES, PUBLISH_YEAR FROM BOOKS");
         ResultSet rs = pst.executeQuery()) {

        // 1. Tablo Modelini Tanımlıyoruz (Sorting ve Editable Koruması Dahil)
        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"BOOK ID", "CATEGORY", "NAME", "AUTHOR", "COPIES", "PAGES", "YEAR"}, 0
        ) {
            // BURAYI EKLEDİK: Kullanıcı hücrelere çift tıklasa bile içine yazamaz
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                if (columnIndex >= 4 && columnIndex <= 6) {
                    return Integer.class;
                }
                return String.class;
            }
        };

        jTable1.setModel(model);
        jTable1.setRowSorter(new TableRowSorter<>(model));

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("BOOK_ID"),
                rs.getString("CATEGORY"),
                rs.getString("NAME"),
                rs.getString("AUTHOR"),
                rs.getInt("COPIES"),
                rs.getInt("PAGES"),
                rs.getInt("PUBLISH_YEAR")
            });
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "DB Error: " + e.getMessage());
    }
}

