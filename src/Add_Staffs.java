    private void addstaff_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addstaff_btnActionPerformed
    String id = txtStaffid.getText().trim();
    String name = txtName.getText().trim();
    String contact = txtContact.getText().trim();

    if (id.isEmpty() || name.isEmpty() || contact.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields!");
        return;
    }

    String sql = "INSERT INTO STAFFS (STAFF_ID, NAME, CONTACT) VALUES (?, ?, ?)";

    try (java.sql.Connection conn = java.sql.DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
         java.sql.PreparedStatement pst = conn.prepareStatement(sql)) {

        pst.setString(1, id);
        pst.setString(2, name);
        pst.setString(3, contact);

        pst.executeUpdate();
        javax.swing.JOptionPane.showMessageDialog(this, "Staff added successfully.");
        
        txtStaffid.setText("");
        txtName.setText("");
        txtContact.setText("");

    } catch (java.sql.SQLException e) {
        logger.log(java.util.logging.Level.SEVERE, "DB Error : ", e);
        javax.swing.JOptionPane.showMessageDialog(this, "Hata: " + e.getMessage());
    }
    }//GEN-LAST:event_addstaff_btnActionPerformed

