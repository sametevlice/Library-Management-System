    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
    String url = "jdbc:mysql://localhost:3306/LIBRARY";
    String dbUser = "root";
    String dbPassword = "Samet9160."; // Replace with your MySQL password

    
    String usernameInput = txtUsername.getText();
    String passwordInput = txtPassword.getText();

    try {
        // connect to sql
        Connection conn = DriverManager.getConnection(url, dbUser, dbPassword);

        // Prepare SQL query to check username and password
        String query = "SELECT * FROM ADMIN WHERE USER_ID=? AND PASSWORD=?";
        PreparedStatement pst = conn.prepareStatement(query);
        pst.setString(1, usernameInput);
        pst.setString(2, passwordInput);

        ResultSet rs = pst.executeQuery();

        if(rs.next()) {
            
            JOptionPane.showMessageDialog(this, "Login successful! Welcome " + rs.getString("NAME"));
            
            Dashboard dsh = new Dashboard();
            dsh.setVisible(true);
            
            this.dispose();
            
        } else {
            
            JOptionPane.showMessageDialog(this, "Incorrect username or password!");
        }

        
        rs.close();
        pst.close();
        conn.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }  
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

